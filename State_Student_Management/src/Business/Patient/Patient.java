package Business.Patient;

/**
 *
 * @author bhaveshraja
 */
public class Patient {
    
    private String emailId;
    private String password;
    private String userName;
    private String name;
    private int age;
    private long mobile;
    private String gender;
    private String address;

    

    public Patient(String emailId, String password, String userName, String name, int age, long mobile, String gender, int address) {
        this.emailId = emailId;
        this.password = password;
        this.userName = userName;
        this.name = name;
        this.age = age;
        this.mobile = mobile;
        this.gender = gender;
        this.address = this.address;
    }
    
}
